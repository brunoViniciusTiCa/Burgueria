    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        pedidoFachada = new PedidoFachada();

        if ( modoTela.equals(TELA_MODO_CADASTRAR)) {

            pedido.setStatusDoPedido(txt_Status.getText());
            pedido.getProduto().setNomeProduto(txt_IdProduto.getText());
            pedido.setQtdProduto(Integer.parseInt(txt_Qtd_Produto.getText()));
            
            
            try {
                pedidoFachada.salvarPedido(pedido);
                Uteis.AlertInfo(this,"Pedido Cadastrado!!!");
            } catch (Exception ex) {
                Uteis.AlertError(this, ex.getMessage());
            }

        }else if (modoTela.equals(TELA_MODO_EDITAR) ){

            try {
                pedido.setStatusDoPedido(txt_Status.getText());
                pedido.getProduto().setNomeProduto(txt_IdProduto.getText());
                pedido.setQtdProduto(Integer.parseInt(txt_Qtd_Produto.getText()));
                

                pedidoFachada.salvarPedido(pedido);
                Uteis.AlertInfo(this,"Pedido inserido com Sucesso!!!");
            } catch (Exception ex) {
                Uteis.AlertError(this,ex.getMessage());
            }

        }
        
            txt_Status.setText("");
            txt_IdProduto.setText("");
            txt_Qtd_Produto.setText("");
       
      
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

